using aqua_api.DTOs;
using aqua_api.Interfaces;
using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Mvc;

namespace aqua_api.Controllers.AquaController
{
    [ApiController]
    [Route("api/aqua/NetOperationType")]
    [Authorize]
    public class NetOperationTypeController : ControllerBase
    {
        private readonly INetOperationTypeService _service;

        public NetOperationTypeController(INetOperationTypeService service)
        {
            _service = service;
        }

        [HttpGet("{id:long}")]
        public async Task<ActionResult<ApiResponse<NetOperationTypeDto>>> GetById(long id, CancellationToken cancellationToken)
        {
            var result = await _service.GetByIdAsync(id);
            return StatusCode(result.StatusCode, result);
        }

        [HttpGet]
        public async Task<ActionResult<ApiResponse<PagedResponse<NetOperationTypeDto>>>> GetAll([FromQuery] PagedRequest request, CancellationToken cancellationToken)
        {
            var result = await _service.GetAllAsync(request);
            return StatusCode(result.StatusCode, result);
        }

        [HttpPost]
        public async Task<ActionResult<ApiResponse<NetOperationTypeDto>>> Create([FromBody] CreateNetOperationTypeDto dto, CancellationToken cancellationToken)
        {            var result = await _service.CreateAsync(dto);
            return StatusCode(result.StatusCode, result);
        }

        [HttpPut("{id:long}")]
        public async Task<ActionResult<ApiResponse<NetOperationTypeDto>>> Update(long id, [FromBody] UpdateNetOperationTypeDto dto, CancellationToken cancellationToken)
        {            var result = await _service.UpdateAsync(id, dto);
            return StatusCode(result.StatusCode, result);
        }

        [HttpDelete("{id:long}")]
        public async Task<ActionResult<ApiResponse<bool>>> Delete(long id, CancellationToken cancellationToken)
        {            var result = await _service.SoftDeleteAsync(id);
            return StatusCode(result.StatusCode, result);
        }
    }
}
